# Include Beaker environment
. /usr/bin/rhts-environment.sh || exit 1
. /usr/share/beakerlib/beakerlib.sh || exit 1

PACKAGE="gdbm"

rlJournalStart
    rlPhaseStartSetup
        rlAssertRpm $PACKAGE
        rlRun "TmpDir=\$(mktemp -d)" 0 "Creating tmp directory"

	rlRun "gcc -o ${TmpDir}/reproducer reproducer.c -lgdbm"

        rlRun "pushd $TmpDir"
    rlPhaseEnd

    rlPhaseStartTest
    	# Reproducer will check all for us... so we just need to run it.
    	rlRun "./reproducer" 0 "Run reproducer."
    rlPhaseEnd

    rlPhaseStartCleanup
        rlRun "popd"
        rlRun "rm -r $TmpDir" 0 "Removing tmp directory"
    rlPhaseEnd
rlJournalPrintText
rlJournalEnd
